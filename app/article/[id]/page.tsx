export default async function NewsDetailsPage({
  params,
}: {
  params: Promise<{ id: string }>;
}) {
  const { id } = await params; // ⬅️ required in Next.js 16
  const articleId = Number(id);
  if (isNaN(articleId)) return notFound();

  const article = news.find((n) => n.id === articleId);
  if (!article) return notFound();

  return (
    <main className="min-h-screen bg-[#F9FAF4] px-6 py-12">
      <div className="max-w-4xl mx-auto bg-white p-8 rounded-2xl shadow-md border border-[#00796B]/20">
        <img
          src={article.image}
          alt={article.title}
          className="w-full h-72 object-cover rounded-xl"
        />

        <span className="text-gray-500 text-sm mt-4 block">{article.date}</span>

        <h1 className="text-3xl font-bold text-[#00796B] mt-2">{article.title}</h1>

        {article.description
        .trim()
        .split("\n\n")
        .map((paragraph, i) => (
          <p key={i} className="mt-6 text-gray-700 text-xl leading-relaxed">
            {paragraph.trim()}
          </p>
        ))}

        <Link
          href="/news"
          className="inline-block mt-8 text-[#00796B] font-semibold hover:underline"
        >
          ← Вернуться к новостям
        </Link>
      </div>
    </main>
  );
}
